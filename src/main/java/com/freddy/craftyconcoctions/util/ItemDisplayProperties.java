package com.freddy.craftyconcoctions.util;

public class ItemDisplayProperties
{
    public final float posX, posY, posZ;
    public final float rotX, rotY, rotZ;

    public ItemDisplayProperties(float posX, float posY, float posZ, float rotX, float rotY, float rotZ)
    {
        this.posX = posX;
        this.posY = posY;
        this.posZ = posZ;
        this.rotX = rotX;
        this.rotY = rotY;
        this.rotZ = rotZ;
    }
}
